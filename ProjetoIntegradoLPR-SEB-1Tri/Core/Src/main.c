void gerarPares(char tabuleiro[size][size]){
	char cartas[16] = {
			'Y', 'Y', 'O', 'O',
			'U', 'U', '.', '.',
			'W', 'W', 'I', 'I',
			'N', 'N', '!', '!'
	};
	for (int linha = 15; linha > 0; linha--) {
	        coluna = rand() % (linha + 1);// limita o rand "%(linha + 1)"
	        char armazenarlinha = cartas[linha]; //armazenar linha dentro
	        cartas[linha] = cartas[coluna];
	        cartas[coluna] = armazenarlinha;

	    }
	    int preencher = 0;
	    for (int linha = 0; linha < size; linha++) {
	        for (int coluna = 0; coluna < size; coluna++) {
	            tabuleiro[linha][coluna] = cartas[preencher++]; /* Atrela valor da Variavel em Froma de matriz; ex: a[3][4] = 12 */
	        }
	    }
	    verifica = 1;
}
void desenharTabuleiro() {
    for (int linha = 0; linha < size; linha++) {
        for (int coluna = 0; coluna < size; coluna++) {
            int Xletra = coluna * 55 + 34; // ajuste para centralizar a letra
            int Yletra = linha * 50 + 32;

            if (revelado[linha][coluna]) {
                char letra[2] = {tabuleiro[linha][coluna], '\0'};
                ST7789_WriteString(Xletra, Yletra, letra, Font_16x26, BLUE, colorBG);
            } else {
                // Desenha uma carta "virada" (preenchida)

                ST7789_DrawImage(coluna * 55 + 30, linha * 50 + 30, 25, 30, cartaPI);


            }
        }
    }
}
void selecionarCartas(bool revelado[size][size], int *cursorX, int *cursorY, int* cartaX, int* cartaY) {

	if (revelado[*cursorY][*cursorX] == true) {
	HAL_Delay(1000);
	ST7789_WriteString(28, 220, "                   ", Font_11x18, colorCard, colorCard);
	return;
	}
	if (selecionadas == 0) {
        *cartaX = *cursorX;
        *cartaY = *cursorY;
        revelado[*cartaY][*cartaX] = true;
       desenharTabuleiro();
        selecionadas++;

    }
	else if (selecionadas == 1) {
        carta2X = *cursorX;
        carta2Y = *cursorY;
        if (carta1X == carta2X && carta1Y == carta2Y) {
            ST7789_WriteString(28, 220, "SELECAO INVALIDA", Font_11x18, YELLOW, colorCard);
            HAL_Delay(1000);
            ST7789_WriteString(28, 220, "                ", Font_11x18, colorCard, colorCard);
            return;
        }



        revelado[carta2Y][carta2X] = true;
        desenharTabuleiro();
        HAL_Delay(1000);
        if (comparar(carta1X, carta1Y, carta2X, carta2Y)) {
            ST7789_WriteString(80, 220, "ACERTOU!", Font_11x18, GREEN, colorCard);
            HAL_Delay(1000);

        } else {
            revelado[carta1Y][carta1X] = false;
            revelado[carta2Y][carta2X] = false;
            ST7789_WriteString(80, 220, "ERROU!", Font_11x18, RED, colorCard);
            HAL_Delay(1000);

        }
        ST7789_WriteString(80, 220, "         ", Font_11x18, colorCard, colorCard);
        HAL_Delay(1000);
        desenharTabuleiro();
        selecionadas = 0;
        tentativaf();

    }

}


